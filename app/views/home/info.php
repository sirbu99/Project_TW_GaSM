                <p>Deșeuri din materiale compozite a căror separare în vederea reciclării implică un cost ridicat.</p>
                <h2>Cum se reciclează lemnul?</h2>
                <p>
                    Deșeurile din lemn sunt duse la centrele de valorificare, unde sunt testate pentru depistarea
                    deșeurilor lemnoase contaminate (lemn vopsit) și a materialelor nedorite precum praf, roci, gunoi
                    menajer. Atunci când este vorba de lemn tratat, produsele de conservare folosite sunt mai
                    problematice. Întrucât nu se poate constata ușor dacă lemnul folosit are sau nu poluanți, nu există
                    companii care să accepte recuperarea materialelor valorificabile conținute în lemn de origini
                    diferite. Riscul ca substanțele dăunătoare să afecteze atmosfera în timpul tocării sau al
                    tratamentului calorific al lemnului este foarte ridicat. De aceea, în general, se renunță la
                    reciclarea lemnului tratat, acesta este incinerat sau gazeificat în instalații special concepute
                    pentru producția de energie, transformat în cărbune de lemn prin procese termice sau în celuloză
                    datorită proceselor chimice, sau eliminate (incinerare sau depozitarea lor în depozite destinate
                    deșeurilor periculoase).<br><br>
                    În prezent, în România, deșeurile de lemn se reciclează la Rădăuți, unde s-a realizat un circuit
                    integrat pentru colectarea și reutilizarea lemnului reciclabil: paleți deteriorați, mobilier vechi,
                    deșeuri de plăci aglomerate din lemn, ambalaje din lemn, deșeuri lemnoase de la construcții și
                    demolări, deșeuri lemnoase municipale sau biomasă.
                </p>
