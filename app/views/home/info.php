                <h1>Cum se reciclează hârtia?</h1>
                <p>Hârtia are o durată de viață redusă, cea de înaltă calitate, cum este hârtia pentru imprimante, poate
                    fi reciclată de maximum 5-7 ori, după care fibrele de celuloză se scurtează prea mult. În acest
                    stadiu, produsul reciclat este de obicei folosit la cofrajele de ouă.
                    La fabrica de reciclare, hârtia este împărțită pe diferite categorii, în funcție de calitate – de la
                    hârtia de imprimantă până la cofraje de ouă. Reciclarea propriu-zisă presupune mărunțirea hârtiei și
                    topirea într-un amestec de apă și diverse substanțe chimice, pentru a separa fibrele de celuloză.
                    Amestecul este curățat de diferite resturi cum sunt, de exemplu, capsele metalice. Mixtura de pulpă
                    rezultată, formată din 1% fibră și 99% apă este apoi întinsă pentru a forma o peliculă, urmează a fi
                    presată pentru a îndepărta mare parte din apă. Pelicula de hârtie trece prin role de uscare și de
                    subțiere până se ajunge la consistența dorită. Aceasta este apoi rulată în suluri și transportată la
                    tipografii, producători de ambalaje, etc.</p>
