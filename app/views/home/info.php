                <h1>Cum se reciclează plasticul?</h1>
                <p>Deși este atât de rezistent la degradare, PET-ul este 100% reciclabil. Din ambalajele reciclate se
                    pot fabrica articole de îmbrăcăminte (ex.: tricouri, bluze „polar”, etc.).

                    Aproximativ 60% din producția mondială de PET/HDPE/poliester din surse naturale este utilizată la
                    fibrele textile. Există mari producători de îmbrăcăminte care folosesc exclusiv fibre obținute din
                    PET/poliester reciclat, dar produsele sunt curate și igienice, procesul de reciclare implicând
                    spălarea, eliminarea impurităților (precum hârtia etichetelor și capacele) și a contaminanților,
                    măcinarea, tocarea, spălarea (din nou), uscarea și prelucrarea materialului în fulgi – materia primă
                    pentru fibre textile sau alte ambalaje din PET.</p>
                <h1>Cum se reciclează flacoanele de plastic?</h1>
                <p>Deșeurile din plastic ajung în unitățile de reciclare de la companii specializate în colectare, de la
                    firme care generează volume mari de astfel de ambalaje sau de la cetățeni. Deșeurile intră pe o
                    bandă transportoare, unde sunt curățate, apoi sortate pe culori, după care intră în linii de
                    măcinare-spălare, unde sunt tocate, spălate repetat şi uscate.<br><br>

                    HDPE (polietilenă de înaltă densitate) și LDPE (polietilenă de joasă densitate) sunt reciclate
                    într-un mod similar cu sticlele PET. Ceea ce diferă este produsul final: acestea sunt transformate
                    la reciclare în materie primă pentru obiecte de plastic durabile – mese, scaune, bănci, coșuri de
                    gunoi, găleți, rigle, etc. LDPE reciclată mai poate fi folosită și la producerea de lemn plastifiat
                    sau folie de plastic. <br><br>

                    Spre deosebire de PET-uri, pungile de plastic din LDPE (polietilenă de joasă densitate) trebuie însă
                    topite după ce au fost tocate și spălate. La sfârșit se obțin niște granule de polietilenă,
                    respectiv polipropilenă, din care se produc folii, saci pentru gunoi, chiar și produse din plastic
                    turnat.</p>

