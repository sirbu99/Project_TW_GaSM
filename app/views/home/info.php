                <h1>Cum colectăm cartonul?</h1>
                <ul>
                    <li>Îndepărtează banda adezivă de pe cutiile de carton.</li>
                    <li>Cartonul trebuie să fie curat, uscat și necontaminat cu resturi alimentare (ex. sosuri – ca în
                        cazul cutiilor de pizza). În cazul în care doar o parte a cartonului este contaminat/murdar, se
                        îndepărtează partea contaminată (care se colectează la deșeurile menajere sau compostabile, după
                        caz), iar partea curată se colectează împreună cu restul deșeurilor din carton.
                    </li>
                    <li>Pentru a ocupa cât mai puțin volum, cartonul se împăturește/turtește, astfel încât să ajungă la
                        o formă cât mai plană.
                    </li>
                </ul>
