                <h1>Cum colectăm metalul și aluminiul</h1>
                <ul>
                    <li>Orice obiect confecționat din metal poate fi reciclat. Metalul se împarte în două grupe: metale
                        neferoase (aluminiu, cupru, zinc etc.) și metale feroase (oțel/inox/fier). Pentru a îți da seama
                        în ce categorie se încadrează folosește un magnet. Metalele neferoase nu sunt magnetice, deci nu
                        se va lipi magnetul de ele, în timp ce metalele feroase sunt magnetice.
                    </li>
                    <li>Metalul poate proveni din multiple surse, de la electronice și electrocasnice mici și mari
                        (unele componente ale acestora sunt confecționate din diferite metale – aur, argint, zinc, oțel,
                        etc.) până la obiecte folosite în construcții sau conserve și doze pentru băuturi.
                    </li>
                    <li>Cel mai frecvent și valoros material reciclabil e aluminiul. Acesta se găsește sub forma dozelor
                        de băuturi răcoritoare, bere, energizante, a flacoanelor cu aerosoli de parfum sau deodorant.
                    </li>
                </ul>
                <h2>Nu se pot recicla:</h2>
                <ul>
                    <li>Recipientele metalice care nu au fost golite de conținut</li>
                    <li>Recipientele metalice de vopsea sau alte produse, contaminate cu solvenți sau alte substanțe
                        periculoase
                    </li>
                    <li>Folia metalizată de la diverse alimente, cum este cea de ciocolată sau de chips-uri</li>
                </ul>
                <h1>Cum se reciclează metalul și aluminiul?</h1>
                <p>Deoarece sortarea în containerele dedicate nu este făcută întotdeauna corespunzător, de obicei la
                    centrele specializate se mai face și o sortare manuală, și apoi una mecanică, ce utilizează un
                    electromagnet, pentru a separa metalele feroase de cele neferoase. Fiecare categorie de metal este
                    apoi procesată într-un mod specific.<br><br>

                    Pentru ca procesul de separare să fie mai facil, în majoritatea cazurilor metalul se reciclează în
                    aceleași containere cu plasticul. Ele sunt apoi alese la stațiile de sortare.<br><br>

                    Deșeurile din metal trebuie colectate pe categorii de metale și duse la centrele de valorificare
                    specializate, unde sunt sortate, spălate, topite, turnate în role, folie, lingou sau sul, apoi
                    transformate în noi obiecte.<br><br>

