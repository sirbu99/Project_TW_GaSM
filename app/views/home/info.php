                    <li>Pungi de carton;</li>
                    <li>Cutii de pizza necontaminate (se poate păstra partea de sus a cutiei care nu se murdărește de
                        obicei);
                    </li>
                    <li>Cartoane-suport pentru cafea;</li>
                    <li>Cartoane/cofraje de ouă;</li>
                    <li>Plicuri din carton (după îndepărtarea componentelor din plastic, cum ar fi „ferestrele” din
                        plastic sau benzile adezive);
                    </li>
                    <li>Role din carton (rămase de la prosoapele din hârtie și hârtia igienică);</li>
                    <li>Cărți poștale și felicitări (după îndepărtarea bateriilor – în cazul felicitărilor muzicale, a
